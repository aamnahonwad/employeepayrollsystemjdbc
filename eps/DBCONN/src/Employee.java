public class Employee {
    int emp_id;
    String name;
    String email;
    String department;
    double salary;
    double bonus;

    public Employee(int emp_id,String name,String email,String department,double salary,double bonus){
        this.emp_id=emp_id;
        this.name=name;
        this.email=email;
        this.department=department;
        this.salary=salary;
        this.bonus=bonus;
    }
}
